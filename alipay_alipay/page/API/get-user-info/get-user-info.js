var app = getApp();
Page({
    data:{
        hasUserInfo:false
    },
    getUserInfo:function(){
        my.getAuthCode({
            scopes:'auth_user',
            fail:(error)=>{console.error('getAuthCode',error)},
            success:()=>{my.getAuthUserInfo({
                fail:(error)=>{console.error('getAuthUserInfo',error)},
                success:(userInfo)=>{
                    console.log(`userInfo:`,userInfo);
                    this.setData({
                        userInfo:userInfo,
                        hasUserInfo:true
                    });
                    abridge.alert({
                        title:JSON.stringify(userInfo)
                    });
                }
            })}
        });
    },
    clear:function(){
        this.setData({
            hasUserInfo:false,
            userInfo:{}
        });
    }
});
